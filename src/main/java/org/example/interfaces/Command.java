package org.example.interfaces;

/**
 * Интерфейс для всех исполняемых команд.
 * <p>
 * Определяет контракт, который должны реализовывать все команды приложения.
 * </p>
 *
 * @author
 * @version 1.0
 */
public interface Command {

    /**
     * Возвращает строковое описание команды (используется в {@code help}).
     *
     * @return описание команды
     */
    public String toString();

    /**
     * Выполняет команду с заданными аргументами.
     * <p>
     * Реализация по умолчанию ничего не делает, чтобы классы могли не переопределять метод,
     * если он им не нужен (хотя в данной архитектуре все команды его переопределяют).
     * </p>
     *
     * @param arg массив аргументов команды
     */
    default void executeCommand(String[] arg) {};

    /**
     * Проверяет корректность аргументов команды.
     *
     * @param arg массив аргументов для проверки
     * @return {@code true}, если аргументы валидны, иначе {@code false}
     */
    public boolean checkArg(String[] arg);
}