package org.example.enums;

/**
 * Перечисление ANSI-кодов цветов для форматирования вывода в консоль.
 * <p>
 * Позволяет выводить сообщения разными цветами для улучшения читаемости.
 * </p>
 *
 * @author
 * @version 1.0
 */
public enum Colors {
    /** Красный цвет (для ошибок). */
    RED("\u001B[31m"),
    /** Зеленый цвет (для успешных операций). */
    GREEN("\u001B[32m"),
    /** Синий цвет (для приглашений ввода). */
    BLUE("\u001B[34m"),
    /** Желтый цвет (для предупреждений). */
    YELLOW("\u001B[33m"),
    /** Черный цвет. */
    BLACK("\u001B[30m"),
    /** Белый цвет. */
    WHITE("\u001B[37m");

    /** ANSI-код цвета. */
    private final String code;

    /**
     * Конструктор цвета.
     *
     * @param code ANSI-код
     */
    Colors(String code) {
        this.code = code;
    }

    /**
     * Возвращает ANSI-код цвета для вставки в строку.
     *
     * @return строковое представление кода цвета
     */
    @Override
    public String toString() {
        return code;
    }
}